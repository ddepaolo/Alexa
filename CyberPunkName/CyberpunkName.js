const handlers = {
    'LaunchRequest': function () {
        this.emit('GetNAME');
    },
    'GetNewFactIntent': function () {
        this.emit('GetNAME');
    },
    'GetNAME': function () {
        // Get a random first and last name from the names lists
        // Use this.t() to get corresponding language data
        const firstArr = this.t('FIRST');
        const firstIndex = Math.floor(Math.random() * firstArr.length);
		const lastArr = this.t('LAST');
        const lastIndex = Math.floor(Math.random() * lastArr.length);
        const randomNAME = firstArr[firstIndex] & ' ' & lastArr[lastIndex];

        // Create speech output
        const speechOutput = this.t('GET_NAME_MESSAGE') + randomNAME;
        this.emit(':tellWithCard', speechOutput, this.t('SKILL_NAME'), randomNAME);
    },
    'AMAZON.HelpIntent': function () {
        const speechOutput = this.t('HELP_MESSAGE');
        const reprompt = this.t('HELP_MESSAGE');
        this.emit(':ask', speechOutput, reprompt);
    },
    'AMAZON.CancelIntent': function () {
        this.emit(':tell', this.t('STOP_MESSAGE'));
    },
    'AMAZON.StopIntent': function () {
        this.emit(':tell', this.t('STOP_MESSAGE'));
    },
};

exports.handler = function (event, context) {
    const alexa = Alexa.handler(event, context);
    alexa.APP_ID = APP_ID;
    // To enable string internationalization (i18n) features, set a resources object.
    alexa.resources = languageStrings;
    alexa.registerHandlers(handlers);
    alexa.execute();
};
